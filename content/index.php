<h2>About the club</h2>

<a href="./photos.php">
   <img class="floating_image" src="photos/0002/0005.jpg" alt="Club photo">
</a>

<p>
Jugglers in the UVic juggling club have been meeting for at least
25 years to practise, learn, and teach each other their skills.
Our membership includes absolute beginners, amateurs at various
levels, and record-holding professional jugglers; we encourage all
levels of jugglers to come out and play or practise at our
bi-weekly meetings. 
<p>
As one of Canada's few university-sponsored juggling clubs, we
provide a unique opportunity for students to learn each others'
technniques, and for beginning jugglers to get inspiration from
experts. The juggling club's range of equipment gives even the
best of us the opportunity to experiment and learn new techniques,
whether clubs, diabolo, and contact, or hats, dice, and plate
spinning.
<p>
No one is too uncoordinated to learn to juggle. Come try it today!
</p>

