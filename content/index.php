<a name="workshops"></a>
<h3>Workshops</h3>

<p>
Workshops are taught on an approximately weekly basis, normally on
Mondays.  Workshops near the start of term will cover beginning
juggling with balls, clubs, and contact balls. Later workshops
will cover more advanced techniques and other equipment, on a
by-request basis.
<p>
If you would like to see anything taught, <a
   href="mailto:juggling@uvic.ca">contact the club</a> or mention
it to one of the exec. If you see anyone at the club practising
any technique, ask them about it and they will be more than happy
to teach you on the spot.
<p>
Watch the <a href="./news.php">news</a> for upcoming workshops.
</p>

