<h2>Links</h2>

<a name="instructional"></a>
<h3>Instructional</h3>

<ul>
   <li> <a href="http://www.frontiernet.net/~steve_glimpse/juggle.html">How to
      Juggle</a> Seems like one of the best online tutorials for how to juggle.
   Give it a shot!! (and then come out to the club, but not necessarily in that
   order!)</li>
   <li> <a href="http://www.twjc.co.uk/tutorials.html" >TWJC Tutorials</a> This
   site has written instructions for lots and lots of 3-ball tricks (unfortunately
   without pictures or video).</li>
   <li> <a href="http://www.yoyoguy.com/info/ball/index2.html" >The Instant
      Juggling Manual</a> The title explains it all - words and pictures. </li>
<li><a
   href="http://www.tujuggle.com/WildcatJugglers/TutorialsByTrickType.htm">
      Wildcat Jugglers Tutorial</a> Lots of good 3-ball tricks and a few for 4 and 5,
   all with excellent videos (including slo-mo!) to help demonstrate. </li>
</ul>

<a name="resources"></a>
<h3>Resources</h3>

<ul>
   <li><a href="http://insanejuggler.tripod.com/" >Juggling Insanity</a> has some
   great variations on traditional tricks.  Check out the &quot;videos&quot; section
   for the most interesting material. </li>
   <li><a href="http://www.contactjuggling.com/" >Contactjuggling.com</a> a great
   resource for contact jugglers. Look for local juggler Ryan Mellors' name on some of
   the tricks. </li>
   <li><a href="http://www.footbag.org/" >Footbag.Org</a> Great resource for
   footbagging (or &quot;hacky-sack&quot; to those of us who grew up in the 80s).
   </li>
   <li><a href="http://www.unicycle.com/" >unicycle.com</a>.  Unicycles. Need we say
   more? </li>
   <li><a href="http://www.lysator.liu.se/juggling/" >Some great videos</a> of great
   past and present jugglers. </li>
</ul>

<a name="performers"></a>
<h3>Performers</h3>

<ul>
   <li><em> Local Circus School:</em> <a
      href="http://www.vancouvercircusschool.ca/">Vancouver Circus School</a> is a
   recently-established school in nearby Vancouver!  Very exciting. </li>
   <li><a href="http://www.diabolo.ca/" >Sean Anderson</a> Home page of the man who
   has been called &quot;the best diabolist in North America&quot;. </li>
   <li><a href="http://www.buildingweight.com/" >Jay Gilligan</a> Jay is my personal
   hero; he has spent his juggling career rebelling against the stereotypes and
   cliches of juggling, with panache, totally hip style, and dazzling technical skill.
   </li>
</ul>

<a name="miscellaneous"></a>
<h3>Miscellany</h3>

<ul>
   <li><a href="http://www.swingmachine.org/issue4/juggling.htm" >The Problem with
      Juggling</a> Highly amusing essay by one Chuck Moulton. </li>
   <li><a href="http://www.obi.com.au/" >http://www.obi.com.au/</a> Site for weird
   balance boards, kind of like souped-up Rola Bola boards. </li>
   <li><a href="http://www.juggler.net/" >Juggler.net</a> offers free, jugging-related
   email addresses. </li>
   <li><a href="http://jugglinglab.sourceforge.net/" >JugglingLab</a> is a great
   juggling animator that can show you zillions of awesome patterns, and it's all
   online through the magic of Java. </li>
   <li><a href="http://www.jongl.de/" >Jongl</a> and <a
      href="http://www.koelnvention.de/software/" >JoePass</a> are the two best pieces
   of software to display, create, and fiddle with juggling patterns from the comfort
   of your own computer. </li>
</ul>
