<h2>Contact information</h2>

<a name="email"></a>
<h3>Email</h3>
<p>Email at <a href="mailto:juggling@uvic.ca">juggling@uvic.ca</a>
is checked regularly by the club exec, and is usually the easiest
way to reach us.</p>

<a name="list"></a>
<h3>Mailing list</h3>
<p>
News about meeting times, workshops, and special events
is posted to our mailing list at
<a href="mailto:jugglingclub@lists.uvic.ca">JugglingClub@lists.uvic.ca</a>.
Non-members are welcome to join the mailing list to keep up-to-date
on club activities; just email your request to the club or subscribe
at <a href="https://lists.uvic.ca/mailman/listinfo/jugglingclub">its
listinfo page</a>.
</p>

<a name="booking"></a>
<h3>Booking performers</h3>
<p>
Although the club exists for learning, not performing, we are in
contact with a number of professional and semi-professional artists
in the Victoria area. Contact us, and we'll gladly pass on your
event's details to the appropriate jugglers.
</p>
