            <a name="driving"></a>
            <h3>Driving</h3>

            <dl>
               <dt>From Vancouver</dt>
               <dd>Take the 99 south towards Seattle. Just after the George Massey tunnel under the Fraser River, take
               the exit for Highway #17, signed for "ferries" and "Victoria via ferries". After about five or 10
               minutes, you'll hit the ferry terminal. Get into one of the lineups marked "Victoria".  The ferry takes
               about 1 hour and 35 minutes. After you get off the ferry, follow the directions to festival sites,
               above.</dd>

               <dt>From Seattle or further south</dt>
               <dd>Take the I5 north and go across the border, take Exit 28 for Highway #17, signed for "Tsawwassen" and
               "Ferries". After taking the exit, keep left and turn left at the traffic light onto Highway 17.  Once
               you're on Highway 17 heading for the ferries, follow directions as above from Vancouver. If you're really
               sneaky you can cut the corner and take Highway #10 from the 99 to Highway 17 in Tsawwassen - guaranteed
               to save you about 10 seconds, or get you lost in darkest Ladner if you aren't carrying an experienced
               navigator.<br>
               If you're feeling adventurous, try taking the ferry from Anacortes over to Sydney.  This will be far
               slower than the BC ferry from Tsawwasen.</dd>
            </dl>

