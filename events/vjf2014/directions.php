            <h3>By bus/ferry</h3>

            <p>
            Laidlaw runs busses from the length and breadth of Vancouver Island, most of the routes will get you to
            Victoria, some via Nanaimo. To take long-distance busses to Victoria from the mainland, take the bus to
            Vancouver and from the Vancouver main bus/train station take the PCL bus to Victoria; see directions
            from downtown, above.
            </p>

            <p>
            Bus destinations in U.S. and Canada:<br>
            Greyhound Bus lines 1-800-231-2222(US)<br>
            1-800-661-8747(Can)
            </p>

            <p>
            For information on the bus to from Vancouver to Victoria, see:<br>
            <a href="http://www.pacificcoach.com/">http://www.pacificcoach.com/</a>
            </p>

            <p>
            For information on the bus to Victoria from up-island, see:<br>
            <a href="http://www.bctravel.com/transport.html">http://www.bctravel.com/transport.html</a><br>
            Laidlaw Coachlines 1-800-318 081
