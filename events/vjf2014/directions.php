            <dl>
               <dt>From Vancouver</dt>
               <dd> Take the PCL bus from the main bus/train station or any of its various pick-up points in Vancouver
               to downtown Victoria (cost: $25 CDN including the ferry).  From downtown Victoria, the bus terminal is
               right across the street from a local bus (BC Transit) stop. From that stop, take the # 4 UVic bus right
               to campus, which puts you at the transit exchange across the road from the SUB. Follow the directions to
               the SUB in "Driving: From Vancouver" if you can't find the SUB from that point!</p>

               <dt>Using BC Transit Only (the Cheapest Possible Way, besides hitching or biking)</dt>
