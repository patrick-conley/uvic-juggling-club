            <h3>Border Information - for US travellers</h3>

            <p>
            The security situation at the border is ever-changing. If you are a juggler (which presumably you are,
            otherwise you wouldn't be reading this), be honest about all the equipment you are bringing and why you are
            bringing it; but do NOT say "I am a street performer" under any circumstances. If you are performing at the
            festival, tell them you won't be getting money for it.  Bring your passport or at the least photo ID, birth
            certificate, and proof of residency (a utility bill, etc).
            </p>

            <p>
            We have extensive experience going south to attend the Portland and Seattle festivals, but little experience
            going north: any US jugglers reading this who know more than we do, please email us and we'll update this
            page. 
            </p>

