            <a name="hotels"></a>
            <h3>Hotels</h3>

            <p>
            There are lots of cheap hotels in Victoria; virtually all of them are near downtown. If you are driving into
            town, many cheap motels can be found along Gorge Road, which can be reached by heading south into town on
            Hwy # 17; turn right at Hillside Avenue, then at the next traffic light (a strange five-way intersection)
            proceed straight through and you will be on Gorge Road. Follow the road and within 5 minutes lots of cheap
            motels will appear. Getting up to the University from this point is easy, follow Hillside Avenue in the
            other direction (east) for about 5km and turn left on Foul Bay Road, you will soon pass the "Welcome to
            UVic" sign. The road at this point makes a sharp right to turn into Ring Road which goes around campus past
            the SUB, where the festival will be held.
            </p>

            <p>
            Several cheaper hotels can also be found along Douglas street (Highway 1) on the way into town; if you are
            coming into town from the ferries on Highway 17, turn right at Cloverdale and left onto Douglas (the next
            major light) to get onto Douglas before the main string of hotels.
            </p>

            <p>
            There are of course the usual chain hotels in town, which are mostly available to book online:
